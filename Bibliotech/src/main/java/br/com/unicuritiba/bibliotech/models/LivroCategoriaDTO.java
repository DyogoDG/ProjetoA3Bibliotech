package br.com.unicuritiba.bibliotech.models;

public class LivroCategoriaDTO {
	
	private String nomeCategoria;
	private String qtdCategoria;
	
	public String getNomeCategoria() {
		return nomeCategoria;
	}
	public void setNomeCategoria(String nomeCategoria) {
		this.nomeCategoria = nomeCategoria;
	}
	public String getQtdCategoria() {
		return qtdCategoria;
	}
	public void setQtdCategoria(String qtdCategoria) {
		this.qtdCategoria = qtdCategoria;
	}
	
}
